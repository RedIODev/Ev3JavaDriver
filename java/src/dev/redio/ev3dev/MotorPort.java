package dev.redio.ev3dev;

/**
 * Represents the output ports A-D on the LEGO Mindstorm Ev3 Brick.
 * @author RedIODev
 */
public enum MotorPort {
    /**
     * Motor output port A.
     */
    OUT_A,
    /**
     * Motor output port B.
     */
    OUT_B,
    /**
     * Motor output port C.
     */
    OUT_C,
    /**
     * Motor output port D.
     */
    OUT_D;
}
